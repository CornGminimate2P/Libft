#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned char	*k;
	unsigned int	i;

	i = 0;
	k = (char *) malloc(ft_strlen(s1) + 1);
	if (!k)
		return (NULL);
	while (s1[i])
	{
		k = s1[i];
		i++;
	}
	k[i] = 0;
	return (k);
}