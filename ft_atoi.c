#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	k;
	int	s;

	i = 0;
	k = 1;
	s = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' && str[i + 1] != '-')
		i++;
	if (str[i] == '-')
	{
		k *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		s *= 10;
		s += str[i] - '0';
		i++;
	}
	s *= k;
	return (s);
}
